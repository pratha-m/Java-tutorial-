package com.company;

public class CWH_04_literals {
   public static void main(String[] args) {
      byte age=34;
      int age2=123;
      long age3=1233334456666l;
      short age4=12;
      char ch='A';
      float f1=5.6f;
      double d1=4.66;
      double d2=4.66d;
      boolean b1=true;
      System.out.println("byte integer :"+age);
      System.out.println("integer :"+age2);
      System.out.println("long integer :"+age3);
      System.out.println("short integer :"+age4);
      System.out.println("character :"+ch);
      System.out.println("float :"+f1);
      System.out.println("double :"+d1);
      System.out.println("double :"+d2);
      System.out.println("bool :"+b1);
      String str="pratham";
      System.out.println("string :"+str);

      // println()-->new line after printing
      // print()-->no new line after printing
   }
}
